/**
 * AI Loading Overlay.
 * Displays a centered spinner with messaging while AI generates content.
 * Shows a green success banner at the top when AI has just finished.
 */
"use client";

import { useState, useEffect } from "react";
import { cn } from "@/lib/utils";
import type { IdeaBlock } from "@/types/sandbox-planner";
import { IDEA_BLOCK_LABELS } from "@/types/sandbox-planner";

/** Field names that AI updates per step — shown in the "soon you will have…" message. */
const STEP_FIELDS: Record<IdeaBlock, string[]> = {
  ideas: [],
  structure_align: ["The Core Problem", "Target Audience", "Primary Success Metrics"],
  refining_icp: ["Day in the Life", "Pain Points & Acute Needs"],
  product_definition_flows: ["Product Summary", "Feature List & Logic", "User Flows"],
  strategic_frameworks: [
    "Value Proposition",
    "JTBD Synthesis",
    "ICP Architect",
    "NABC Pitch",
    "Optimization Suggestions",
  ],
};

interface AiLoadingOverlayProps {
  /** The step currently being generated by AI. */
  step: IdeaBlock;
  /** Whether the AI has just completed (show success banner). */
  justCompleted?: boolean;
}

/** Full-area overlay shown while AI is generating content for a step. */
export default function AiLoadingOverlay({
  step,
  justCompleted = false,
}: AiLoadingOverlayProps) {
  const [showSuccess, setShowSuccess] = useState(justCompleted);

  // Auto-dismiss success banner after 5 seconds
  useEffect(() => {
    if (!showSuccess) return;
    const timer = setTimeout(() => setShowSuccess(false), 5000);
    return () => clearTimeout(timer);
  }, [showSuccess]);

  // Show success banner when justCompleted changes to true
  useEffect(() => {
    if (justCompleted) setShowSuccess(true);
  }, [justCompleted]);

  const fields = STEP_FIELDS[step] ?? [];
  const fieldList = fields.join(", ");

  if (showSuccess && !justCompleted) return null;

  return (
    <div className="relative flex flex-col items-center justify-center min-h-64">
      {/* Success banner */}
      {showSuccess && (
        <div
          className={cn(
            "absolute top-0 left-0 right-0 px-4 py-3 rounded-lg text-sm font-medium text-center",
            "bg-green-500 text-white"
          )}
          role="status"
        >
          Success! Our AI has started updating {fieldList}
        </div>
      )}

      {/* Spinner + message */}
      {!justCompleted && (
        <div className="flex flex-col items-center gap-4 mt-8">
          {/* Animated spinner icon */}
          <div className="w-16 h-16 rounded-full bg-accent flex items-center justify-center animate-pulse">
            <svg
              className="w-8 h-8 text-white"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
              strokeWidth={2}
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                d="M13 10V3L4 14h7v7l9-11h-7z"
              />
            </svg>
          </div>

          <p className="text-base font-medium text-text-primary">
            AI is working...
          </p>
          {fields.length > 0 && (
            <p className="text-sm text-text-muted text-center max-w-md">
              Soon you will have {fieldList} updated
            </p>
          )}
        </div>
      )}
    </div>
  );
}
